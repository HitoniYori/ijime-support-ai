IJIME_PREVENTION_ACT_FULL_TEXT = r"""
いじめ防止対策推進法（全文）

第一章　総則
（目的）
第一条　この法律は、いじめが、いじめを受けた児童等の教育を受ける権利を著しく侵害し、その心身の健全な成長及び人格の形成に重大な影響を与えるのみならず、その生命又は身体に重大な危険を生じさせるおそれがあるものであることに鑑み、児童等の尊厳を保持するため、いじめの防止等（いじめの防止、いじめの早期発見及びいじめへの対処をいう。以下同じ。）のための対策に関し、基本理念を定め、国及び地方公共団体の責務を明らかにし、並びにいじめの防止等のための対策に関する基本的な方針の策定について定めることにより、いじめの防止等のための対策を総合的かつ効果的に推進することを目的とする。

（定義）
第二条　この法律において「いじめ」とは、児童等に対して、当該児童等が在籍する学校に在籍している等当該児童等と一定の人的関係にある他の児童等が行う心理的又は物理的な影響を与える行為（インターネットを通じて行われるものを含む。）であって、当該行為の対象となった児童等が心身の苦痛を感じているものをいう。
２　この法律において「学校」とは、学校教育法（昭和二十二年法律第二十六号）第一条に規定する小学校、中学校、高等学校、中等教育学校及び特別支援学校（幼稚園を除く。）をいう。
３　この法律において「児童等」とは、学校に在籍する児童又は生徒をいう。
４　この法律において「保護者」とは、親権を行う者（親権を行う者のないときは、未成年後見人）をいう。

（基本理念）
第三条　いじめの防止等のための対策は、いじめが全ての児童等に関係する問題であることに鑑み、児童等が安心して学習その他の活動に取り組むことができるよう、学校の内外を問わずいじめが行われなくなるようにすることを旨として行われなければならない。
２　いじめの防止等のための対策は、全ての児童等がいじめを行わず、及び他の児童等に対して行われるいじめを認識しながらこれを放置することがないようにするため、いじめが児童等の心身に及ぼす影響その他のいじめの問題に関する児童等の理解を深めることを旨として行われなければならない。
３　いじめの防止等のための対策は、いじめを受けた児童等の生命及び心身を保護することが特に重要であることを認識しつつ、国、地方公共団体、学校、地域住民、家庭その他の関係者の連携の下、いじめの問題を克服することを目指して行われなければならない。

（いじめの禁止）
第四条　児童等は、いじめを行ってはならない。

（国の責務）
第五条　国は、第三条の基本理念（以下「基本理念」という。）にのっとり、いじめの防止等のための対策を総合的に策定し、及び実施する責務を有する。

（地方公共団体の責務）
第六条　地方公共団体は、基本理念にのっとり、いじめの防止等のための対策について、国と協力しつつ、当該地域の状況に応じた施策を策定し、及び実施する責務を有する。

（学校の設置者の責務）
第七条　学校の設置者は、基本理念にのっとり、その設置する学校におけるいじめの防止等のために必要な措置を講ずる責務を有する。

（学校及び学校の教職員の責務）
第八条　学校及び学校の教職員は、基本理念にのっとり、当該学校に在籍する児童等の保護者、地域住民、児童相談所その他の関係者との連携を図りつつ、学校全体でいじめの防止及び早期発見に取り組むとともに、当該学校に在籍する児童等がいじめを受けていると思われるときは、適切かつ迅速にこれに対処する責務を有する。

（保護者の責務等）
第九条　保護者は、子の教育について第一義的責任を有するものであって、その保護する児童等がいじめを行うことのないよう、当該児童等に対し、規範意識を養うための指導その他の必要な指導を行うよう努めるものとする。
２　保護者は、その保護する児童等がいじめを受けた場合には、適切に当該児童等をいじめから保護するものとする。
３　保護者は、国、地方公共団体、学校の設置者及びその設置する学校が講ずるいじめの防止等のための措置に協力するよう努めるものとする。
４　第一項の規定は、家庭教育の自主性が尊重されるべきことに変更を加えるものと解してはならず、また、前三項の規定は、いじめの防止等に関する学校の設置者及びその設置する学校の責任を軽減するものと解してはならない。

（財政上の措置等）
第十条　国及び地方公共団体は、いじめの防止等のための対策を推進するために必要な財政上の措置その他の必要な措置を講ずるよう努めるものとする。

第四章　いじめの防止等に関する措置
（学校におけるいじめの防止等の対策のための組織）
第二十二条　学校は、当該学校におけるいじめの防止等に関する措置を実効的に行うため、当該学校の複数の教職員、心理、福祉等に関する専門的な知識を有する者その他の関係者により構成されるいじめの防止等の対策のための組織を置くものとする。

（いじめに対する措置）
第二十三条　学校の教職員、地方公共団体の職員その他の児童等からの相談に応じる立場にある者は、児童等からいじめに係る相談を受けたこと等によりいじめの事実があると思われるときは、いじめを受けたと思われる児童等が在籍する学校への通報その他の適切な措置をとるものとする。
２　学校は、前項の規定による通報を受けたときその他当該学校に在籍する児童等がいじめを受けていると思われるときは、速やかに、当該児童等に係るいじめの事実の有無の確認を行うための調査を開始するとともに、その結果を当該学校の設置者に報告するものとする。
３　学校は、前項の規定による確認の結果、いじめの事実が確認された場合には、いじめをやめさせ、及びその再発を防止するため、当該学校の複数の教職員によって、いじめを受けた児童等及びその保護者に対する支援並びにいじめを行った児童等に対する指導及びその保護者に対する助言を継続的に行うものとする。
４　学校は、前項の場合において必要があると認めるときは、いじめを受けた児童等がいじめを受けた教室以外の場所において学習することができるようにする等、いじめを受けた児童等その他の児童等が安心して教育を受けられるようにするために必要な措置を講ずるものとする。
５　学校は、当該学校の教職員がいじめを受けた児童等といじめを行った児童等との間で争いを仲裁する場合において、当該いじめを受けた児童等が心身の苦痛を感じているときには、当該いじめを受けた児童等の意向にかかわらず、当該いじめを行った児童等から謝罪させること等によって安易に解決を図ることなく、当該教職員以外の教職員が加わって組織的に対応するものとする。
６　第三項の規定による支援又は指導を行った学校の教職員は、当該支援又は指導が適切に行われているか、及びいじめが再発していないかについて、第二十二条の組織を活用して継続的に確認を行うものとする。
７　学校は、いじめが犯罪行為と認められるときは所轄警察署と連携してこれに対処するものとし、当該学校に在籍する児童等の生命、身体又は財産に重大な被害が生じるおそれがあるときは、直ちに所轄警察署に通報し、適切に援助を求めなければならない。

（学校の設置者による措置）
第二十四条　学校の設置者は、前条第二項の規定による報告を受けたときは、必要に応じ、その設置する学校に対し、必要な支援及び指導を行うものとする。

（校長及び教員による懲戒）
第二十五条　校長及び教員は、当該学校に在籍する児童等がいじめを行っている場合であって、教育上必要があると認めるときは、学校教育法第十一条の規定に基づき、適切に、当該児童等に対して懲戒を加えるものとする。

（出席停止制度の適切な運用）
第二十六条　市町村の教育委員会は、いじめを行った児童等の保護者に対し学校教育法第三十五条第一項（同法第四十九条において準用する場合を含む。）の規定に基づき当該児童等の出席停止を命ずる等、いじめを受けた児童等その他の児童等が安心して教育を受けられるようにするために必要な措置を速やかに講ずるものとする。

（学校相互間の連携協力体制の整備）
第二十七条　地方公共団体は、いじめを受けた児童等がいじめを行った児童等と同じ学校に在籍していない場合であっても、学校がいじめを受けた児童等に対して適切な支援を行い、又は学校がいじめを行った児童等に対して適切な指導を行うことができるよう、学校相互間の連携協力体制を整備するものとする。

第五章　重大事態への対処
（学校の設置者又はその設置する学校による調査）
第二十八条　学校の設置者又はその設置する学校は、次に掲げる場合には、その事態（以下「重大事態」という。）に対処し、及び当該重大事態と同種の事態の発生の防止に資するため、速やかに、当該学校の設置者又はその設置する学校の下に組織を設け、質問票の使用その他の適切な方法により当該重大事態に係る事実関係を明確にするための調査を行うものとする。
一　いじめにより当該学校に在籍する児童等の生命、心身又は財産に重大な被害が生じた疑いがあると認めるとき。
二　いじめにより当該学校に在籍する児童等が相当の期間学校を欠席することを余儀なくされている疑いがあると認めるとき。
２　学校の設置者又は学校は、前項の規定による調査を行ったときは、当該調査に係るいじめを受けた児童等及びその保護者に対し、当該調査に係るいじめの事実関係その他の必要な情報を適切に提供するものとする。
３　第一項の規定により学校が調査を行う場合において、当該学校の設置者は、同項の調査及び前項の規定による情報の提供について必要な指導及び支援を行うものとする。

（国立大学に附属して設置される学校に係る対処）
第二十九条　国立大学法人は、その設置する学校において重大事態が発生した場合には、第二十八条第一項各号に掲げる場合には、当該国立大学法人の学長を通じて、文部科学大臣に、重大事態が発生した旨を、報告しなければならない。
２　前項の規定による報告を受けた文部科学大臣は、当該重大事態への対処又は当該重大事態と同種の事態の発生の防止のため必要があると認めるときは、前条第一項の調査の結果について調査を行い、その結果に基づき、当該国立大学法人に対し、必要な措置を講ずるよう勧告することができる。

（公立の学校に係る対処）
第三十条　地方公共団体の長は、第二十八条第一項各号に掲げる場合には、当該地方公共団体の教育委員会を通じて、重大事態が発生した旨を、報告しなければならない。
２　前項の規定による報告を受けた地方公共団体の長は、当該重大事態への対処又は当該重大事態と同種の事態の発生の防止のため必要があると認めるときは、附属機関を設けて、第二十八条第一項の規定による調査の結果について調査を行うことができる。
３　地方公共団体の長は、前項の規定による調査を行ったときは、その結果を議会に報告しなければならない。
４　第二項の規定による調査を行った地方公共団体の長は、その結果に基づき、必要があると認めるときは、教育委員会に対し、必要な措置を講ずるよう勧告することができる。

（私立の学校に係る対処）
第三十一条　学校法人（私立学校法（昭和二十四年法律第二百七十号）第三条に規定する学校法人をいう。）は、その設置する学校において重大事態が発生した場合には、第二十八条第一項各号に掲げる場合には、当該都道府県知事を通じて、重大事態が発生した旨を、報告しなければならない。
２　前項の規定による報告を受けた都道府県知事は、当該重大事態への対処又は当該重大事態と同種の事態の発生の防止のため必要があると認めるときは、附属機関を設けて、第二十八条第一項の規定による調査の結果について調査を行うことができる。
３　都道府県知事は、前項の規定による調査を行ったときは、その結果を当該学校法人を所轄する知事に報告しなければならない。
４　第二項の規定による調査を行った都道府県知事（当該学校法人を所轄する知事を除く。）は、その結果に基づき、必要があると認めるときは、当該学校法人を所轄する知事に対し、私立学校法第六条の規定による措置その他の必要な措置を講ずるよう求めることができる。

（文部科学大臣又は都道府県知事の指導、助言及び援助）
第三十三条　文部科学大臣又は都道府県知事は、第二十九条から第三十一条第一項までの規定による報告を受けた場合、重大事態への対処に関する都道府県知事又は市町村の教育委員会の事務の適正な処理を図るため、必要な指導、助言又は援助を行うことができる。

第六章　雑則
（学校評価における留意事項）
第三十四条　学校の評価を行う場合において、いじめの事実が隠蔽されず、並びにいじめの実態把握及びいじめの防止等のための対策が適切に行われているかどうかを評価の項目として加えなければならない。

（高等専門学校における措置）
第三十五条　高等専門学校は、当該高等専門学校に在籍する学生に係るいじめに相当する行為の防止、当該行為の早期発見及び当該行為への対処のための対策に関し必要な措置を講ずるよう努めるものとする。
"""

