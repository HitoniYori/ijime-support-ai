BASIC_POLICY_ON_BULLYING = r"""
いじめの防止等のための基本的な方針（平成29年3月14日改定）

５．いじめの定義
個々の行為が「いじめ」に当たるか否かの判断は，表面的・形式的にすることなく，いじめられた児童生徒の立場に立つことが必要である。
けんかやふざけ合いであっても、見えない所で被害が発生している場合もあるため，背景にある事情の調査を行い，児童生徒の感じる被害性に着目し，いじめに該当するか否かを判断するものとする。

６．いじめの解消の定義
いじめは，単に謝罪をもって安易に解消とすることはできない。
いじめが「解消している」状態とは，少なくとも次の２つの要件が満たされている必要がある。
(1) いじめに係る行為が止んでいること
被害者に対する心理的又は物理的な影響を与える行為（インターネットを通じて行われるものを含む。）が止んでいる状態が相当の期間継続していること。この相当の期間とは，少なくとも３か月を目安とする。
(2) 被害児童生徒が心身の苦痛を感じていないこと
いじめに係る行為が止んでいるかどうかを判断する時点において，被害児童生徒がいじめの行為により心身の苦痛を感じていないと認められること。

４．重大事態への対処
児童生徒や保護者から，いじめにより重大な被害が生じたという申立てがあったときは，その時点で学校が「いじめの結果ではない」あるいは「重大事態とはいえない」と考えたとしても，重大事態が発生したものとして報告・調査等に当たる義務がある。
"""

